]).

-export_type([
]).

%%----------------------------------------------------------------------------------------------------------------------
%% Macros & Records & Types
%%----------------------------------------------------------------------------------------------------------------------

%%----------------------------------------------------------------------------------------------------------------------
%% Exported Functions
%%----------------------------------------------------------------------------------------------------------------------


%% @doc `Char'が英字[a-z A-Z]かどうかを判定する
-spec is_alpha(Char :: char()) -> boolean().
is_alpha(Char) ->
    $a =< Char andalso Char =< $z orelse $A =< Char andalso Char =< $Z.

%% @doc `Char'が数文字[0-9]かどうかを判定する
-spec is_num(Char :: char()) -> boolean().
is_num(Char) ->
    $0 =< Char andalso Char =< $9.


%% @doc `Char'が英数字かどうかを判定する
-spec is_alpha_num(Char :: char()) -> boolean().
is_alpha_num(Char) ->
    is_alpha(Char) orelse is_num(Char).

